    private void performOperation(String operation){
        if(operation.equals("="))
            jTextField1.setText("");
        else if (setTextField1){
            jTextField1.setText(jTextField1.getText() + jTextField2.getText() + " " + operation + " ");
        }else {
            jTextField1.setText(jTextField1.getText() + " " + operation + " ");
        }
        /*else if(!operation.equals("") )
            jTextField1.setText(jTextField1.getText() + jTextField2.getText() + " " + operation + " ");
        else if(!newNumber){
            jTextField1.setText(jTextField1.getText() + " " + operation + " ");
        }*/
        
        String number = jTextField2.getText().replaceAll(",", "");
        boolean updateTextField = true;
        
        if(operator.equals("+"))
            currentResult = currentResult + Double.parseDouble(number);
        else if (operator.equals("-"))
            currentResult = currentResult - Double.parseDouble(number);
        else if (operator.equals("*"))
            currentResult = currentResult * Double.parseDouble(number);
        else if (operator.equals("/"))
            currentResult = currentResult / Double.parseDouble(number);
        else{
            currentResult = Double.parseDouble(number);
            updateTextField = false;
        }
        
        operator = operation;
        String regex = "(\\d)(?=(\\d{3})+$)";
        String result;
        if((currentResult == Math.floor(currentResult)) && !Double.isInfinite(currentResult)
                && currentResult < Integer.MAX_VALUE){
            result = Integer.toString((int)currentResult);
            result = result.replaceAll(regex, "$1,");
        }
        else{
            result = Double.toString(currentResult);
        }
        
        if(updateTextField)
            jTextField2.setText(result);
       
        newNumber = true;
        setTextField1 = true;
    }
    
