    //for square root, reciprocal and square
    private void performOperation(Double answer, String operation){
        DecimalFormat df = new DecimalFormat("#.#####");
        int index;
        String number = jTextField1.getText();
            
        if(operator.equals("") || operator.equals("=")){
            jTextField1.setText("");
        }
        else{
            index = number.lastIndexOf(operator);
            if(index+2<number.length() && index!= -1){
                number = number.substring(index+2, number.length());
                jTextField1.setText(jTextField1.getText().substring(0, index+2));
            }
            else if(index!=-1)
                number = jTextField2.getText();
        }
        if(number.equals(""))
            number = jTextField2.getText();
        
        
        jTextField2.setText(df.format(answer));
        jTextField1.setText(jTextField1.getText() + operation + "(" +number +")");
        setTextField1 = false;
    }
    
